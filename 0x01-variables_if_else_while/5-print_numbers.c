#include <stdio.h>

/**
 * main - print all the single digits of base 10
 *
 * Description: prints all the single digits
 * 10 
 * Return: 0 is successful 
 */

int main(void)
{
	int a;

	for (a = 0; a <= 9; a++)
		printf("%d", a);
	printf("\n");
}
